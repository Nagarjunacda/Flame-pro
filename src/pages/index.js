import { Inter } from "next/font/google";
import { homePageUrl } from "@/utils/urls";
import { handleServerSideProps } from "@/utils/handleServerSideData";
import { useMediaQuery } from "react-responsive";
import RenderTrays from "@/components/RenderTrays";
import Head from "next/head";
const inter = Inter({ subsets: ["latin"] });

export default function Home(props) {
  const { data } = props;
  const trayData = data?.acf?.content_blocks;
  const additionalDataExt = data?.acf_fields;

  const isDesktop = useMediaQuery({ query: "(min-width:900px)" });

  return (
    <>
      <Head>
        <title>FlamePro</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.png" />
      </Head>
      <main>
        {trayData ? (
          <RenderTrays
            trayData={trayData}
            additionalDataExt={additionalDataExt}
          />
        ) : (
          <p>This Page Under Development</p>
        )}
      </main>
    </>
  );
}

export async function getServerSideProps(context) {
  const { data, error } = await handleServerSideProps(homePageUrl);
  if (error) {
    return {
      props: {
        data: null,
      },
    };
  }

  return {
    props: {
      data,
    },
  };
}
